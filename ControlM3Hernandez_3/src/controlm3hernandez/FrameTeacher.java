    private void buttonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateActionPerformed
        // leer el dni jcombobox coger el dni del student;
        // searchstudent(dni) 
        // objeto teacher.addStudent(student que viene de search)

        Teacher teacher = new Teacher();

        teacher.setName(textName.getText());
        teacher.setSurname(textSurname.getText());
        teacher.setSalary(Float.parseFloat(textSalary.getText()));
        teacher.setDni(textDni.getText());
        teacher.setEmail(textEmail.getText());
        teacher.setDateAlta(textDate.getText());
        jTextArea1.setText(teacher.toString());
        String info = "";
        if (DataClass.validateTEDni(teacher) == true) {
            DataClass.ALteacher.add(teacher);

        } else if (DataClass.validateTEDni(teacher) == false) {

            JOptionPane.showMessageDialog(rootPane, "That DNI already exists.", "TEACHER DNI DUPLICATION", HEIGHT);
            textDni.setText("");

        }

        for (Teacher t : DataClass.ALteacher) {
            info += t + "\n\r";
        }
        System.out.println(info);

    }//GEN-LAST:event_buttonCreateActionPerformed

