    private void jCheckBoxStudentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBoxStudentsActionPerformed
        if (jCheckBoxStudents.isSelected()) {
            jComboBoxStudent.removeAllItems();

            for (Student s : DataClass.ALstudent) {
                jComboBoxStudent.addItem(s.getDni());
            }

            jComboBoxStudent.setVisible(true);

        } else {
            jComboBoxStudent.setVisible(false);
        }
    }//GEN-LAST:event_jCheckBoxStudentsActionPerformed

    public static Teacher search(String dni) {
        Teacher resultado = null;
        for (Teacher teacher : DataClass.ALteacher) {
            if (teacher.getDni().equals(dni)) {

                resultado = teacher;
                break;
            }
        }
        return resultado;
    }

